package com.sample.school.vo;

public class Department {
	
	private int no;
	private String name;
	
	public Department() {}

	public int getNo() {
		return no;
	}

	public void setNo(int no) {
		this.no = no;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	

	
	
}
